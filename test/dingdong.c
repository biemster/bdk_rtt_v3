static int dingdong(int argc, char *argv[]) {
	rt_kprintf("dingdong!\n");
	int dac_wr_len = 0;

	rt_pin_mode(AUDIOAMP_PIN, PIN_MODE_OUTPUT);
	rt_pin_write(AUDIOAMP_PIN, PIN_HIGH);

	audio_device_init();
	audio_device_open();
	audio_device_set_rate(8000);
	uint8_t *buffer = NULL;
	uint8_t tempbuf[READ_SIZE *2] = {0};

	while(1) {
		buffer = audio_device_get_buffer(RT_NULL);
		if(dac_wr_len >= sizeof(dingdongwav)) {
			audio_device_put_buffer(buffer);
			break;
		}

		// convert from single channel to required stereo
		for(int i = 0; i < READ_SIZE; i++) {
			tempbuf[i*2] = (dingdongwav[i +dac_wr_len] -127) *3;
			tempbuf[i*2 +1] = tempbuf[i*2];
		}
		
		memcpy(buffer, tempbuf, READ_SIZE *2);
		dac_wr_len += READ_SIZE /2; // /2 is just to make the sound a bit longer, but actually shouldn;t be there
		audio_device_write(buffer, READ_SIZE *2);
	}
	
	audio_device_close();

	rt_pin_write(AUDIOAMP_PIN, PIN_LOW);
	return 0;
}

